# tuple in python 
# tuple are ordered collection of data items.
# they stire multiple items in a single variable.
# tuple  items are separated by comas and enclosed within round brackets().
# they are unchangeable/immutable means they cannot be altered after creation.
tup =(1,2,3,4,5,6,78,"red")
# tup[0] = 90
# tuple doesn't support assignment
print(type(tup), tup)
print(len(tup))
print(tup[0])
print(tup[-3])

